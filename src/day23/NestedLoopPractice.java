package day23;

public class NestedLoopPractice {
public static void main(String[] args) {
	
		for(int y = 1;y<=3;y++) {
			
			for(char z = 'a';z<='z';z++) {
				System.out.print(z+" ");
				
			}
			System.out.println("\n###################################################");
			
		}
		
	
	
	
	
}
}
